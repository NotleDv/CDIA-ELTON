turno = input('Digite seu turno de estudo: ')

if (turno == 'M'):
    print('Bom Dia!')
elif (turno == 'V'):
    print('Boa Tarde!')
elif (turno == 'N'):
    print('Boa Noite!')
else:
    print('Valor Inválido')    